def get_gemini_answer(user_query, text_context, user_mode):
    """
    هذه الدالة مسؤولة عن إرسال الطلب، النصوص المستخرجة من الكتاب، وسؤال الطالب 
    إلى سيرفرات Google لتتم معالجتها عبر نموذج Gemini 1.5 Flash الخارق.
    """
    try:
        # إعداد الموديل مع تمرير التوجيهات النظامية الصارمة كـ System Instruction كما تدعمها جوجل
        model = genai.GenerativeModel(
            model_name='gemini-1.5-flash',
            system_instruction=SYSTEM_PROMPT
        )
        
        # صياغة الرسالة الشاملة التي تدمج السياق والوضع الحالي والسؤال لتوجيه الموديل بدقة
        full_user_message = (
            f"[بوابة الاستخدام الحالية في المنصة: {user_mode}]\n\n"
            f"نص كتاب المعهد الأزهرى المرفوع كـ سياق معتمد:\n{text_context}\n\n"
            f"طلب المستخدم وسؤاله الحالي الذي يحتاج إجابة:\n{user_query}"
        )
        
        # ضبط الحرارة لمنع التأليف وضمان الدقة
        config = genai.types.GenerationConfig(
            temperature=0.4
        )
        
        # توليد المحتوى من السيرفر مباشرة
        response = model.generate_content(full_user_message, generation_config=config)
        return response.text
    except Exception as e:
        return f"⚠️ حدث خطأ أثناء الاتصال بسيرفر جيميناي: {str(e)}"


def extract_text_from_pdf(pdf_file):
    """
    مهمة هذه الدالة هي استقبال ملف الـ PDF الحقيقي المرفوع من الطالب أو المعلم،
    والدخول إلى أعماقه لاستخراج كافة النصوص والحروف وإعادتها كـ سياق نصي متصل.
    """
    reader = PdfReader(pdf_file)
    extracted_text = ""
    
    # المرور على كافة صفحات ملف المعهد صفحة تلو الأخرى
    for page in reader.pages:
        text = page.extract_text()
        if text:
            extracted_text += text + "\n"
            
    return extracted_text


def text_to_speech_player(text_to_speak):
    """
    الميزة الأسطورية: استقبال رد الوكيل المكتوب، وتحويله في الذاكرة المؤقتة إلى ملف
    صوتي MP3 ناطق بالفصحى وعرض مشغل صوتي تفاعلي يمنع ملل الطلاب أثناء المذاكرة.
    """
    try:
        # توليد الصوت باللغة العربية الفصحى المبسطة وبسرعة عادية واضحة
        tts = gTTS(text=text_to_speak, lang='ar', slow=False)
        
        # إنشاء وعاء في ذاكرة الرام العشوائية (RAM) لحفظ الصوت دون استهلاك مساحة السيرفر
        sound_file = io.BytesIO()
        tts.write_to_fp(sound_file)
        
        # عرض مشغل الصوت الأنيق المدمج بخصائص التشغيل والتحكم في الصوت والتمرير
        st.audio(sound_file, format='audio/mp3')
    except Exception as e:
        st.warning("⚠️ مشغل الصوت واجه ضغطاً مؤقتاً، يمكنك قراءة التوضيح المكتوب في الأعلى ببساطة.")

